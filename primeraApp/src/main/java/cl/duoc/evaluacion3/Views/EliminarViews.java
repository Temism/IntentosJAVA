    private void BEliminarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BEliminarPeliculaActionPerformed
        // TODO add your handling code here:
        boolean peliculaEncontrada = false;
        titulo = TxtBusquedaPelicula.getText();
        if(!titulo.isEmpty()){
            for (MovieDTO m : lista) {
                if (titulo.equals(m.getTitulo())) {
                    id = m.getID();
                    peliculaEncontrada = true;
                    break;  
                }
            }
           try {
               if(!peliculaEncontrada && !titulo.isEmpty() ){
                    JOptionPane.showMessageDialog(null,"No existe ninguna pelicula con ese titulo","Error", JOptionPane.ERROR_MESSAGE);
                }else{
                   int eliminar = JOptionPane.showConfirmDialog(null,"Estas seguro que deseas eliminar esta pelicula","Confirmacion",JOptionPane.YES_NO_OPTION);
                    if (eliminar == JOptionPane.YES_OPTION){
                       JOptionPane.showMessageDialog(null, "Película eliminada de la base de datos", titulo.toString() , JOptionPane.INFORMATION_MESSAGE);
                     servicio.Eliminar(id); 
                     servicio.listar();
                    } else {
                        JOptionPane.showConfirmDialog(null, "Operacion cancelada", "Cancelar", JOptionPane.PLAIN_MESSAGE);
                    }     
                    }
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, "Error al eliminar la película " + ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
            }
        }else{
            JOptionPane.showMessageDialog(null, "Ingrese un titulo para buscar", "Campo titulo vacio", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_BEliminarPeliculaActionPerformed

