    private void BBuscarPeliculaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BBuscarPeliculaActionPerformed
        // TODO add your handling code here:
        titulo = TxtBusquedaPelicula.getText();
        servicio.BuscarTitulo(titulo);
        lista = servicio.BuscarTitulo(titulo);
        boolean peliculaEncontrada = false;
        
        for (MovieDTO m : lista) {
            if (titulo.equals(m.getTitulo())) {
                id = m.getID();
                peliculaEncontrada = true;
                break;  
            }
        }

        if (!peliculaEncontrada) {
            JOptionPane.showMessageDialog(null, "Pelicula no encontrada", "Error", JOptionPane.ERROR_MESSAGE);
            return; 
        }else{
            JOptionPane.showMessageDialog(null,"Pelicula Encontrada ", "Exito",JOptionPane.PLAIN_MESSAGE);
        }

    }//GEN-LAST:event_BBuscarPeliculaActionPerformed


